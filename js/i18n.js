let currentLang = 'en';

function t(key) {
  return i18n[currentLang][key] || i18n['en'][key] || key;
}

function changeLang(lang) {
  currentLang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.documentElement.lang = lang;
  localStorage.setItem('lang', lang);
  document.getElementById('langSelect').value = lang;
}

async function initLang() {
  const savedLang = localStorage.getItem('lang');
  if (savedLang && i18n[savedLang]) {
    currentLang = savedLang;
    document.getElementById('langSelect').value = savedLang;
    changeLang(currentLang);
    return;
  }
  
  try {
     const response = await fetch('https://ipapi.co/json/');
     const data = await response.json();
     if(data.country_code === 'CN' || data.country_code === 'TW' || data.country_code === 'HK'){
       currentLang = data.country_code === 'CN' ? 'zh' : 'zh-TW';
     } else if (data.country_code === 'JP') {
       currentLang = 'ja';
     } else if (data.country_code === 'KR') {
       currentLang = 'ko';
     } else if (data.country_code === 'RU') {
        currentLang = 'ru';
     } else if (data.country_code === 'ES' || data.country_code === 'MX' || data.country_code === 'AR' || data.country_code === 'CO') {
        currentLang = 'es';
     } else if (data.country_code === 'FR') {
        currentLang = 'fr';
     } else if (data.country_code === 'DE') {
        currentLang = 'de';
     } else if (data.country_code === 'PT' || data.country_code === 'BR') {
        currentLang = 'pt';
     } else if (data.country_code === 'AE' || data.country_code === 'SA' || data.country_code === 'EG') {
        currentLang = 'ar';
     } else if (data.country_code === 'TR') {
        currentLang = 'tr';
     } else if (data.country_code === 'VN') {
        currentLang = 'vi';
     } else if (data.country_code === 'TH') {
        currentLang = 'th';
     } else if (data.country_code === 'ID') {
        currentLang = 'id';
     }
  } catch (error) {
     console.warn('IP detect failed, defaulting to English', error);
  }
  
  if(!i18n[currentLang]) {
    currentLang = 'en';
  }
  
  document.getElementById('langSelect').value = currentLang;
  changeLang(currentLang);
}